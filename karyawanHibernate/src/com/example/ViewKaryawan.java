    private void loadKaryawanToTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tabelKaryawan.getModel();
        tableModel.setRowCount(0);
        ControlKaryawan kontrol = new ControlKaryawan();
        List<Karyawan> karyawanList = kontrol.getAllKaryawan();
        for(Karyawan karyawan : karyawanList) {
            tableModel.addRow(new Object[]{
                karyawan.getID(), 
                karyawan.getNama(), 
                karyawan.getJabatan(), 
                karyawan.getNo_telp(), 
                karyawan.getAlamat(), 
                karyawan.getStatus()
            });
        }
    }
    
    private int getSelectedKaryawanId(String nama, String jabatan, String no_telp) {
        ControlKaryawan kontrol = new ControlKaryawan();
        List<Karyawan> karyawanList = kontrol.getAllKaryawan();
        
        for(Karyawan karyawan : karyawanList) {
            if(karyawan.getNama().equals(nama) && karyawan.getJabatan().equals(jabatan) && karyawan.getNo_telp().equals(no_telp)) {
                return karyawan.getID();
            }
        }
        
        return -1;
    }
    
