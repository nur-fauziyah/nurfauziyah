    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedKaryawanId == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus dari tabel!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int confirmed = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if(confirmed == JOptionPane.YES_OPTION) {
            try {
                ControlKaryawan kontrol = new ControlKaryawan();
            
                Karyawan karyawan = kontrol.getKaryawanById(selectedKaryawanId);
                if(karyawan != null) {
                    kontrol.deleteKaryawan(karyawan);
                    JOptionPane.showMessageDialog(this,"Data berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    loadKaryawanToTable();
                    resetForm();
                }
            } catch(Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

