    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nama = iNama.getText().trim();
        String jabatan = iJabatan.getText().trim();
        String no_telp = iNo_telp.getText().trim();
        String alamat = iAlamat.getText().trim();
        String status = iStatus.getSelectedItem().toString();
        
        if(nama.isEmpty() || jabatan.isEmpty() || no_telp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        boolean isDataExist = false;
        DefaultTableModel model = (DefaultTableModel) tabelKaryawan.getModel();
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            String namaTabel = (String) model.getValueAt(i, 1);
            String jabatanTabel = (String) model.getValueAt(i, 2);
            String noTelpTabel = (String) model.getValueAt(i, 3);
            if (nama.equals(namaTabel) && jabatan.equals(jabatanTabel) && no_telp.equals(noTelpTabel)) {
                isDataExist = true;
                break;
            }
        }
        
        if (isDataExist) {
            JOptionPane.showMessageDialog(this, "Data karyawan sudah ada!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Karyawan karyawan = new Karyawan();
                karyawan.setNama(nama);
                karyawan.setJabatan(jabatan);
                karyawan.setNo_telp(no_telp);
                karyawan.setAlamat(alamat);
                karyawan.setStatus(status);

                ControlKaryawan kontrol = new ControlKaryawan();
                kontrol.saveKaryawan(karyawan);

                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                loadKaryawanToTable();
                resetForm();
            } catch(Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
//        if(selectedKaryawanId != -1) {
//            JOptionPane.showMessageDialog(this, "Data ini sudah ada! Silakan lakukan update.", "Error", JOptionPane.ERROR_MESSAGE);
//            return;
//        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tabelKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelKaryawanMouseClicked
        // TODO add your handling code here:
        int selectedRow = tabelKaryawan.getSelectedRow();
        if(selectedRow != -1) {
            String nama = tabelKaryawan.getValueAt(selectedRow,1).toString();
            String jabatan = tabelKaryawan.getValueAt(selectedRow,2).toString();
            String no_telp = tabelKaryawan.getValueAt(selectedRow,3).toString();
            String alamat = tabelKaryawan.getValueAt(selectedRow,4).toString();
            String status = tabelKaryawan.getValueAt(selectedRow,5).toString();
            
            
            iNama.setText(nama);
            iJabatan.setText(jabatan);
            iNo_telp.setText(no_telp);
            iAlamat.setText(alamat);
            iStatus.setSelectedItem(status);
            
            selectedKaryawanId = getSelectedKaryawanId(nama, jabatan, no_telp);
        }
    }//GEN-LAST:event_tabelKaryawanMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(selectedKaryawanId == -1) {
            JOptionPane.showMessageDialog(this,"Pilih data yang ingin diperbarui.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String nama = iNama.getText().trim();
        String jabatan = iJabatan.getText().trim();
        String no_telp = iNo_telp.getText().trim();
        String alamat = iAlamat.getText().trim();
        String status = iStatus.getSelectedItem().toString();
        
        if(nama.isEmpty() || jabatan.isEmpty() || no_telp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Semua kolom harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            ControlKaryawan kontrol = new ControlKaryawan();
            Karyawan karyawan = kontrol.getKaryawanById(selectedKaryawanId);
            karyawan.setNama(nama);
            karyawan.setJabatan(jabatan);
            karyawan.setNo_telp(no_telp);
            karyawan.setAlamat(alamat);
            karyawan.setStatus(status);
            
            kontrol.updateKaryawan(karyawan);
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadKaryawanToTable();
            resetForm();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memperbarui data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

